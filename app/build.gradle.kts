tasks.register("downloadIperf3") {
    val destDir = File("${projectDir}/src/main/jniLibs")
    doLast {
        val targets = mapOf(
            "arm64-v8a" to "arm64-v8a",
            "armeabi-v7a" to "armeabi-v7a",
            "x86_64" to "x86_64",
            "x86" to "x86"
        )
        val baseUrl = "https://raw.githubusercontent.com/davidBar-On/android-iperf3/gh-pages/libs/"
        targets.forEach { (remoteName, localName) ->
            val abiDir = File(destDir, localName)
            abiDir.mkdirs()
            val f = File(abiDir, "libiperf3.so")
            if (!f.exists()) {
                println("Downloading Android iperf3 for $localName...")
                try {
                    URI(baseUrl + remoteName + "/iperf3.21").toURL().openStream().use { input ->
                        f.outputStream().use { output ->
                            input.copyTo(output)
                        }
                    }
                } catch (e: Exception) {
                    println("Failed to download $remoteName: ${e.message}")
                }
            }
        }
    }
}
tasks.named("preBuild").configure {
    dependsOn("downloadIperf3")
}

